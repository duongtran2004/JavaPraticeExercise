package duongtran.JavaOOPExercises.basicOOP.Ex3;

public class Rectangle {
    //attributes
    double width;
    double height;
    //constructor

    public Rectangle(double width, double height) {
        this.width = width;
        this.height = height;
    }
    //getter and setters

    public double getWidth() {
        return width;
    }

    public void setWidth(double width) {
        this.width = width;
    }

    public double getHeight() {
        return height;
    }

    public void setHeight(double height) {
        this.height = height;
    }
}
